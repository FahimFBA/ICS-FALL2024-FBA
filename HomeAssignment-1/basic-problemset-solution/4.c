#include <stdio.h>

int main()
{
    int a; // declare an integer variable
    float b; // declare a float variable
    char c; // declare a character variable

    a = 17; // initialize the integer variable
    b = 3.508; // initialize the float variable
    c = 'W'; // initialize the character variable

    printf("Integer value = %d\n", a); // print the integer value
    printf("Floating point value = %.3f\n", b); // print the float value
    printf("Character value = %c\n", c); // print the character value
}
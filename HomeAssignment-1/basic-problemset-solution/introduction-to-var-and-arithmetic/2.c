#include <stdio.h>

int main()
{
    int a, b; // declare two integer variables
    a = 18, b = 7; // initialize the integer variables

    printf("%d + %d = %d\n", a, b, a + b); // print the sum of two integers
    printf("%d - %d = %d\n", a, b, a - b); // print the difference of two integers
    printf("%d * %d = %d\n", a, b, a * b); // print the product of two integers
    printf("%d / %d = %d\n", a, b, a / b); // print the division of two integers
    printf("%d %% %d = %d\n", a, b, a % b); // print the remainder of two integers

    // you can create separate variables for the sum, difference, product, division, and remainder
}